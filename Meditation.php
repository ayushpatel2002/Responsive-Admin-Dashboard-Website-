                <h3>6) Transcendental Meditation</h3>
                A common form of meditation is Transcendental Meditation. The scientific community has conducted various investigations on this practice.<br>
                It is more customizable than mantra meditation, using a mantra or series of words that are specific to each practitioner.
                <br><br>
                You can sitting on a comfortable chair with your feet on the ground and hands in your lap uncrossed and followed by keeping eyes closed and taking deep breaths.
                <br> Then, you can start chanting mantras according to your preference.
                <br><br>
                Some examples are: -
                <ol>
                    <li><i>Om Namah Shivaya</i></li>
                    <li><i>Aham Prema</i></li>
                    <li><i>Sat Nam</i></li>
                </ol>
