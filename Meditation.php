                <h3>2) Spiritual meditation</h3>
                Eastern religions, such as Hinduism and Daoism, as well as Christian faith, use spiritual meditation.<br><br>
                It’s similar to prayer in that you reflect on the silence around you and seek a deeper connection with your God or Universe.<br>
                Essential oils such as myrrh, sage, sandalwood, cedar, palo santo and etc. are commonly used to heighten the spiritual experience.<br>
                Monks suggests that the easiest way to begin is to sit quietly and focus on your breath till at least 20 minutes.<br> <br>
                Its benefits includes: -
                <ul>
                    <li>Spiritual meditation releases and settles our thoughts and emotions</li>
                    <li>It relaxes your nervous system and helps your body unwind from stress</li>
                    <li>It helps you to let go of the past and sink in peace</li>
                </ul>
                Spiritual meditation can be practiced at home or in a place of worship. This practice is beneficial for those who thrive in silence and seek spiritual growth.
            </div>
