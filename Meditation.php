            <div>
                <h3>7) Progressive relaxation</h3>
                Progressive relaxation, often known as body scan meditation, is a technique for lowering physical tension and fostering relaxation.
                <br>
                This type of meditation frequently entails gradually tightening and relaxing one muscle group at a time throughout the body.<br>
                In some cases, it may also encourage you to imagine a gentle wave flowing through your body to help release any tension. This, meditation is generally performed before bedtime often used to relieve stress. You feel more calm and relaxed after a session of progressive relaxation.
            </div>
            <hr>
            <hr>
            <div>
                <h3>8) Loving-kindness meditation</h3>
                Loving-kindness meditation is used to strengthen feelings of compassion, kindness, and acceptance toward oneself and others.
                <br>
                It typically involves opening the mind to receive love from others and then sending a series of well wishes to loved ones, friends, acquaintances, and all living beings.<br>
                Because this type of meditation is intended to promote compassion and kindness, it may be ideal for those holding feelings of anger or resentment.
            </div>
            <hr>
            <hr>
            <div>
                <h3>9) Visualization Meditation</h3>
                Visualization meditation is a technique focused on enhancing feelings of relaxation, peace, and calmness by visualizing positive scenes or images.<br>
                With this practice, it’s important to imagine the scene vividly and use all five senses to add as much detail as possible.<br>
                Another form of visualization meditation involves imagining yourself succeeding at specific goals, which is intended to increase focus and motivation.<br>
                <br><br>
                Many people use visualization meditation to boost their mood, reduce stress levels, and promote inner peace.
