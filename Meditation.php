                <h3>1) Mindfulness Meditation</h4>
                    The most common meditation practice in the West is mindfulness meditation, which is based on Buddhist teachings.
                    <br> <br> In mindfulness meditation, you pay attention to your thoughts as they pass through your mind. You don’t judge the thoughts or become involved with them. You simply observe and take note of any patterns.
                    <br>
                    This practice combines concentration with awareness. You may find it helpful to focus on an object or your breath while you observe any bodily sensations, thoughts, or feelings.
                    <br>
                    This sort of meditation is helpful for people who don't have access to an instructor because it can be done alone.
