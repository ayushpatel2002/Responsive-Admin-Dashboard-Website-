                <h3>4) Movement Meditation</h3>
                Although most people think of yoga when they hear movement meditation, this practice may include walking through the woods, gardening, qigong, and other gentle forms of motion.<br><br>
                It’s an active form of meditation where the movement guides you.<br>
                Movement meditation is good for people who find peace in action and prefer to let their minds wander.<br><br>
                You can begin movement meditation by standing peacefully and feeling sensations in the body in an open space.
                You can try out following body movements while doing this yoga: -
                <ul>
                    <li>Stretch out your arms</li>
                    <li>Rotate your shoulders </li>
                    <li>Gently rotating your neck side to side</li>
                    <li>Try out shaking your arms and legs.</li>
                </ul>
