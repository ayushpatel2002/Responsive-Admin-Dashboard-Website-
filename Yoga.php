                <h3>How to do it: -</h3>
                <ol>
                    <li class="howtodo">First, get on all fours</li>
                    <li class="howtodo">Now, place your wrists under the shoulders and knees under the hips</li>
                    <li class="howtodo">Lift your hips off the floor and push them back towards your heels</li>
                    <li class="howtodo">Try to straighten your legs while pushing your hips back</li>
                    <li class="howtodo">Firmly, press through your palms and rotate your inner elbows towards each other</li>
                    <li class="howtodo">Keep your legs engaged and hollow out your abdominals</li>
                    <li class="howtodo">Hold for 5-8 breaths and then release</li>
                </ol>
